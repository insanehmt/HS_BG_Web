DATA_PATH = os.path.join(os.path.dirname(__file__), "..", "data", "bg_comps.json")
with open(DATA_PATH, encoding='utf-8') as f:
    existing = json.load(f)

existing_ids = {c['id'] for c in existing}
new_comps = [c for c in neutral_comps if c['id'] not in existing_ids]
combined = existing + new_comps

with open(DATA_PATH, 'w', encoding='utf-8') as f:
    json.dump(combined, f, ensure_ascii=False, indent=2)

print(f"Total: {len(combined)} comps (added {len(new_comps)})")
tiers = {"S":[], "A":[], "B":[], "C":[]}
for c in combined:
    tiers[c['tier']].append(c['name'])
for t, names in tiers.items():
    print(f"  Tier {t} ({len(names)}): {', '.join(names)}")
